    FORMATDATETIME (d, 'yyyy-MM-dd') AS DE
    , DAY_OF_WEEK (  PARSEDATETIME (d, 'yyyy-MM-dd') ) AS DWK
    , 'N'
    , 'N'
    , null
    , now()
